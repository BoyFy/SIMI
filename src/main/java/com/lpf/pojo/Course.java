package com.lpf.pojo;

public class Course {
    public int couNo;
    public int couNumber;
    public String couName;
    public float credits;
    public float couTime;

    public int getCouNo() {
        return couNo;
    }

    public void setCouNo(int couNo) {
        this.couNo = couNo;
    }

    public int getCouNumber() {
        return couNumber;
    }

    public void setCouNumber(int couNumber) {
        this.couNumber = couNumber;
    }

    public String getCouName() {
        return couName;
    }

    public void setCouName(String couName) {
        this.couName = couName;
    }

    public float getCredits() {
        return credits;
    }

    public void setCredits(float credits) {
        this.credits = credits;
    }

    public float getCouTime() {
        return couTime;
    }

    public void setCouTime(float couTime) {
        this.couTime = couTime;
    }
}
